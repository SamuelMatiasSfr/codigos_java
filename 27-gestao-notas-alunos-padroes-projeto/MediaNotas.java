public class MediaNotas {

    private int id;
    private float media;
    
    public MediaNotas(int id, float media) {
        this.id = id;
        this.media = media;
    }

    public int getId() {
        return id;
    }

    public float getMedia() {
        return media;
    }

    public void setMedia(int media) {
        this.media = media;
    }

}
